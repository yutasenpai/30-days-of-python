# EXERCISE 3 - POWERS LIST GENERATOR
# ======================================================================
print("""
 _______  _______  _     _  _______  ______    _______ 
|       ||       || |   | ||       ||    _ |  |       |
|    _  ||   _   || |   | ||    ___||   | ||  |  _____|
|   |_| ||  | |  || | _ | ||   |___ |   |_||_ | |_____ 
|    ___||  |_|  || ||_|| ||    ___||    __  ||_____  |
|   |    |       ||       ||   |___ |   |  | | _____| |
|___|    |_______||_______||_______||___|  |_||_______|
""")
# ======================================================================

powers_list = [(i, 1, i, i**2, i**3, i**4, i**5) for i in range(11)]

count = 1
total_items = len(powers_list)

print("█" * 70)
print(" 🔢 [EXERCISE 3] POWERS MATRIX:")
for row in powers_list:
    if count == 1:
        print(f"[{row},")    
    elif count == total_items:
        print(f"{row}]")        
    else:
        print(f"{row},")
    count += 1 
print("█" * 70 + "\n")


