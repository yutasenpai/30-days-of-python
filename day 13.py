# EXERCISE 7 - LAMBDA CONDITIONAL EVEN/ODD CHECKER
# ======================================================================
print("""
 _       _______  __   __  _______  ______   _______ 
| |     |   _   ||  |_|  ||  _    ||      | |   _   |
| |     |  |_|  ||       || |_|   ||  _    ||  |_|  |
| |     |       ||       ||       || | |   ||       |
| |___  |       ||       ||  _   | | |_|   ||       |
|     |_|   _   || ||_|| || |_|   ||       ||   _   |
|_______|__| |__||_|   |_||_______||______| |__| |__|
""")
# ======================================================================

nums = [12, 7, 15, 22, 4, 9]
even_odd_checker = lambda n: "EVEN" if n % 2 == 0 else "ODD"

checked_nums = [f"{n} is {even_odd_checker(n)}" for n in nums]

print("█" * 70)
print(f" 🧮 [EXERCISE 7] LAMBDA EVEN/ODD CHECK:")
print(f"    Original Numbers: {nums}")
print(f"    Checked Results:  {checked_nums}")
print("█" * 70 + "\n")